// Функция для поиска команды по ID
export const getTeamById = (id: number): FootballTeamInfo | undefined => {
    return footballTeams.find(team => team.id === id);
};

// Функция для поиска команды по названию
export const getTeamByName = (name: string): FootballTeamInfo | undefined => {
    return footballTeams.find(team =>
        team.name.toLowerCase().includes(name.toLowerCase()) ||
        team.shortName.toLowerCase().includes(name.toLowerCase())
    );
};

// Функция для получения команд по лиге/стране
export const getTeamsByLeague = (league: string): FootballTeamInfo[] => {
    const leagueTeamIds: { [key: string]: number[] } = {
        'premier-league': [1, 2, 3, 4, 5, 6, 7, 8],
        'la-liga': [9, 10, 11, 12, 13, 14],
        'serie-a': [15, 16, 17, 18, 19, 20],
        'bundesliga': [21, 22, 23, 24],
        'ligue-1': [25, 26, 27, 28],
        'russian-premier-league': [29, 30, 31, 32, 33, 34],
        'other': [35, 36, 37, 38, 39, 40]
    };

    const teamIds = leagueTeamIds[league] || [];
    return footballTeams.filter(team => teamIds.includes(team.id));
};

